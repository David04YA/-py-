# 比较运算符 
# == 相等为True 否则为False
# != 不相等为True 否则为False
# > 大于为True 否则为False
# < 小于为True 否则为False
# >= 大于等于为True 否则为False
# <= 小于等于为True 否则为False
bool_1= True
bool_2= False
print("bool_1==bool_2的结果是:",bool_1==bool_2)
print("bool_1!=bool_2的结果是:",bool_1!=bool_2)
print(type(bool_1),type(bool_2))